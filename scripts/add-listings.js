export async function addListings() {
  try {
    console.log("Starting to add 10 home listings...");
    const results = [];
    
    for (let i = 0; i < listings.length; i++) {
      const listing = listings[i];
      const docRef = await addDoc(collection(database, "listings"), listing);
      results.push({ id: docRef.id, title: listing.title });
      console.log(`✓ Added listing ${i + 1}/10: ${listing.title} (ID: ${docRef.id})`);
    }
    
    console.log("\n✅ Successfully added all 10 listings!");
    console.log("Listing IDs:", results.map(r => r.id));
    return results;
  } catch (error) {
    console.error("Error adding listings:", error);
    throw error;
  }
}

